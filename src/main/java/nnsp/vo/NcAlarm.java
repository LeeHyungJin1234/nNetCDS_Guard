package nnsp.vo;

import java.io.Serializable;

@SuppressWarnings("serial")
public class NcAlarm implements Serializable {
	String noa_type;
	String noa_detail;
	String noa_reg_date;
	
	public String getNoa_detail() {
		return noa_detail;
	}
	public void setNoa_detail(String noa_detail) {
		this.noa_detail = noa_detail;
	}
	public String getNoa_type() {
		return noa_type;
	}
	public void setNoa_type(String noa_type) {
		this.noa_type = noa_type;
	}
	public String getNoa_reg_date() {
		return noa_reg_date;
	}
	public void setNoa_reg_date(String noa_reg_date) {
		this.noa_reg_date = noa_reg_date;
	}
	
}